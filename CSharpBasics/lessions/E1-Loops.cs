public class Loop
{
    public void LearnLoops()
    {
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i + ". Nepal is beautiful country.");
        }

        int j = 1;
        while (j <= 10)
        {
            Console.WriteLine(j + ". Nepal is beautiful country.");
            j++;
        }
    }
}